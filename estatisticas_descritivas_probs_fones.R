            '<pad>')



# Função para calcular as estatísticas descritivas
estatisticas <- sapply(lista_vetores, function(x) {
  c(media = round(mean(x), digits = 3), 
    mediana = round(median(x), digits = 3), 
    desvio_padrao = round(sd(x), digits = 3), 
    minimo = round(min(x), digits = 3), 
    maximo = round(max(x), digits = 3))
})


# Adicionando os labels como coluna na matriz de estatísticas
tabela <- cbind(Label = labels, t(estatisticas))
tabela<-as.data.frame(tabela)

library(openxlsx)
setwd('C:/Users/55229/Desktop/caed/tabelas_estagio')
write.xlsx(x = tabela, file='stats_fones-table.xlsx')



