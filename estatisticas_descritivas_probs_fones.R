
lista_vetores<-readRDS('minha_lista.rds')

labels <- c('p', 'b', 't', 'd', 'k', 'g', 'f', 'v', 's', 'z', 'ʃ', 'ʒ', 'ʁ',
            'tʃ', 'dʒ', 'm', 'n', 'ɲ', 'ɾ', 'w', 'j', 'w~','j~', 'ʎ', 'l',
